x = 1j  #complex
x = ["Ali", "Ahmad", "Laila", "Maryam"] # list
x = ("Apple", "Banana", "Cherry") #tuple
x = range(6) #range

x = {"name", "Ali", "age", "28"} # dict
x = {"apple", "grapes", "Cherry"} #set
x = frozenset({"apple", "banana", "cherry"}) # frozenset
x= True # boolean
x = b"hello" # byte
x = bytearray(10) #bytearray
x = memoryview(bytes(5)) # memoryview
x = None # none type  

